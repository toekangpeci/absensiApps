    private void b_simpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_b_simpanMouseClicked
        // TODO add your handling code here:
        String sql="Insert into jabatan values(?,?,?,?,?,?)";
        String akses="";
        if (cbApp.isSelected()){
        akses="1";} else {akses="0";};
        if (cbRep.isSelected()){
        akses=akses+"1";} else {akses=akses+"0";};        
        if (cbMan.isSelected()){
        akses=akses+"1";} else {akses=akses+"0";};
        
        try{            
            PreparedStatement stat=conn.prepareStatement(sql);
            stat.setString(1,txt_kode.getText());
            stat.setString(2,txt_nama.getText());
            stat.setString(3,txt_keterangan.getText());  
            stat.setString(4,txt_lembur.getText());
            stat.setString(5,txt_dinas.getText());
            stat.setString(6,akses);
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan");
            kosong();
            txt_kode.requestFocus();
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null,"Data Gagal Disimpan : "+ e);
        }
    }//GEN-LAST:event_b_simpanMouseClicked

