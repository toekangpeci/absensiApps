    public void getDataid(String id){
        b_simpan.setVisible(false);
        b_edit.setVisible(true);
        jLabel16.setText("               Ubah Data Jabatan");        
        txt_kode.setText(home.id_jabatan);
        String a = txt_kode.getText();
        txt_kode.setEnabled(false);
        String akses="";
        try{            
            String sql="SELECT kode_jabatan,nama_jabatan,keterangan,upah_lembur,upah_dinas,panelAppRep from jabatan where kode_jabatan='"+a+"'";
            Statement st = conn.createStatement(); ResultSet rs = st.executeQuery(sql);             
            while (rs.next()){
             txt_kode.setText(rs.getString("kode_jabatan"));
             txt_nama.setText(rs.getString("nama_jabatan"));
             txt_lembur.setText(rs.getString("upah_lembur"));
             txt_dinas.setText(rs.getString("upah_dinas"));
             txt_keterangan.setText(rs.getString("keterangan"));
             akses=(rs.getString("panelAppRep"));
             }
            if (akses.equals("111")){
            cbApp.setSelected(true); 
            cbRep.setSelected(true);
            cbMan.setSelected(true);}
            else if (akses.equals("110")){
            cbApp.setSelected(true); 
            cbRep.setSelected(true);
            cbMan.setSelected(false);}
            else if (akses.equals("101")){
            cbApp.setSelected(true); 
            cbRep.setSelected(false);
            cbMan.setSelected(true);}
            else if (akses.equals("100")){
            cbApp.setSelected(true); 
            cbRep.setSelected(false);
            cbMan.setSelected(false);}
            else if (akses.equals("000")){
            cbApp.setSelected(false); 
            cbRep.setSelected(false);
            cbMan.setSelected(false);}
            else if (akses.equals("010")){
            cbApp.setSelected(false); 
            cbRep.setSelected(true);
            cbMan.setSelected(false);}
            else if (akses.equals("001")){
            cbApp.setSelected(false); 
            cbRep.setSelected(false);
            cbMan.setSelected(true);}
            else if (akses.equals("011")){
            cbApp.setSelected(false); 
            cbRep.setSelected(true);
            cbMan.setSelected(true);}
            
            
            
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null,"data department gagal dipanggil : "+ e);
            }
    }
    
